#include "libft.h"

int	ft_atoi(const char *str)
{
	int	a;
	int	b;
	int	i;

	a = 0;
	b = 1;
	i = 0;
	while (str[a] == 32 || str[a] == '\t' || str[a] == '\n' || str[a] == '\v'
		|| str[a] == '\f' || str[a] == '\r')
		a++;
	if (str[a] == 45 || str[a] == 43)
	{
		if (str[a] == 45)
			b = -b;
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9')
	{
		i = i * 10;
		i = i + str[a] - 48;
		a++;
	}
	i = i * b;
	return (i);
}
