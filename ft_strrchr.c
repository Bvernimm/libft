#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (str[i] != '\0')
		i++;
	while (i >= 0 && str[i] != c)
		i--;
	if (c == s[i])
		return (&str[i]);
	return (NULL);
}
